//Different data types array

package day0_Array;

public class Ex2_ArrayTypes 
{
	public static void main(String[] args)
	{
		byte arr1[] = {10,11,12,13};
		short arr2[] = {20,21};
		int arr3[] = {30,31};
		long arr4[] = {1000,2000};
		float arr5[] = {10.0f,20.0f,30.0f};
		double arr6[] = {100.0,200.1};
		boolean arr7[] = {true,false, true};
		char arr8[] = {'a','b','c'};
		
		//byte array
		System.out.println(arr1[0]);
		System.out.println(arr1[1]);
		System.out.println(arr1[2]);
		System.out.println(arr1[3]);
		
		//short array
		System.out.println(arr2[0]);
		System.out.println(arr2[1]);
		
		//int array
		System.out.println(arr3[0]);
		System.out.println(arr3[1]);

		//long array
		System.out.println(arr4[0]);
		System.out.println(arr4[1]);

		//float array
		System.out.println(arr5[0]);
		System.out.println(arr5[1]);
		System.out.println(arr5[2]);
		
		//double array
		System.out.println(arr6[0]);
		System.out.println(arr6[1]);
		
		//boolean array
		System.out.println(arr7[0]);
		System.out.println(arr7[1]);
		System.out.println(arr7[2]);
		
		//char array
		System.out.println(arr8[0]);
		System.out.println(arr8[1]);
		System.out.println(arr8[2]);
	}
}
