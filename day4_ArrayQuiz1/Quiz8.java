package day4_ArrayQuiz1;

public class Quiz8 
{
	public static void main(String[] args)
	{
		int[] arr = new int[] {10,20,30,40,50};
		for(int i=0; i<arr.length; i++)
			System.out.print(arr[i]+" ");
		
		int[] ar = {1,2,3,4,5};
		for(int i=0; i<ar.length; i++)
			System.out.print(ar[i]+" ");
	}
}

